# Data types

# ! Strings
# Subscript  
print("Hello"[-1])

# ! Integers
print(21 + 31)

# large numbers
print(123_234_123)

# ! Float
print(2.213123)

# ! Boolean
True
False

# Data type of a variable can be checked with type()
name = "Jack"
print(type(name))

# f-Strings help in printing different data types 
# without actually converting it ourselves
score = 0
print(f"your score is {score}")

# format numbers
result = 12.4534
print("{:.2f}".format(result))
