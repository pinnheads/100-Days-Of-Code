# If-Else Control Flow
a = 10
b = 5
if a > 5:
    print(a)
else:
    print(b)

# Nested If-Else
a = 10
b = 5
if a > 10:
    print(a)
elif b > 5:
    print(b)
else:
    print(a,b)

